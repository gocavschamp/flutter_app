class BannerList {
  String _icon;
  String _url;

  String get icon => _icon;
  String get url => _url;

  BannerList({
      String icon, 
      String url}){
    _icon = icon;
    _url = url;
}

  BannerList.fromJson(dynamic json) {
    _icon = json["icon"];
    _url = json["url"];
  }

  Map<String, dynamic> toJson() {
    var map = <String, dynamic>{};
    map["icon"] = _icon;
    map["url"] = _url;
    return map;
  }

}

